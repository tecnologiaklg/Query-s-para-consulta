DTENEG,
SUM((SELECT CAB.VLRNOTA 
    FROM TGFCAB CAB
    WHERE
    CAB.CODTIPOPER IN ('3100','3103')
    AND CAB.DTNEG >= '01/07/2021'
    AND CAB.DTNEG <= '29/07/2021'
    AND CAB.CODVEND IN ('1','7','12','26','28','31','33')
    AND CAB.PENDENTE = 'N'
    AND CAB.AD_N_NEG_VEND IS NULL)) AS COM NEGOCIAÇÃO,
