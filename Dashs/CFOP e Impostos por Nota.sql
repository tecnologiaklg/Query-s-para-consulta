SELECT
/*ITE.NUNOTA,
CAB.NUMNOTA,*/
ITE.CODCFO,
SUM (ITE.VLRTOT),
SUM (ITE.VLRIPI),
SUM (ITE.VLRICMS),
SUM (ITE.VLRSUBST)
/*CAB.TIPMOV*/

FROM
TGFITE ITE,
TGFCAB CAB

WHERE
ITE.NUNOTA = CAB.NUNOTA
AND CAB.CODEMP = '3'
AND CAB.DTNEG >= '01/06/2021'
AND CAB.DTNEG <= '30/06/2021'
/*AND CAB.STATUSNFE = 'A'*/
AND CAB.CODTIPOPER IN ('3001','3003','3004','3006','3007','3200','3002','3203','3204','3205','3207','3208','3209','3210','3211','3260','3261','3262')

GROUP BY
ITE.CODCFO