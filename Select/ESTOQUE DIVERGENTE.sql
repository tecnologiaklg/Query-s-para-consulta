SELECT
PRO.CODPROD,
PRO.DESCRPROD,
PRO.USOPROD,
EST.CODEMP,
EST.CODLOCAL,
LOC.DESCRLOCAL,
EST.CONTROLE,
EST.ESTOQUE

FROM
TGFEST EST
JOIN TGFPRO PRO ON EST.CODPROD = PRO.CODPROD
JOIN TGFLOC LOC ON EST.CODLOCAL = LOC.CODLOCAL


WHERE
EST.CODLOCAL IN ('10480000','10390000','10370000','10450000','10420000','10470000','10010000','10380000','14200000','20020000','30010000','30050000','10020000','10440000','20260000','10081300','20010000','30070000','10510101','10510200')