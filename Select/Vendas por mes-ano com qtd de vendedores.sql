SELECT
SUM(BBB.VENDAS) AS VENDAS,
COUNT(BBB.QTDVEND) AS QTDVEND,
BBB.PERIODO


FROM
(SELECT
SUM(AAA.VENDAS) AS VENDAS,
AAA.QTDVEND AS QTDVEND,
AAA.PERIODO

FROM
(SELECT
CAB.VLRNOTA AS VENDAS,
LTRIM(TO_CHAR(CAB.DTNEG, 'mm/yyyy'), '0') AS PERIODO,
CAB.CODVEND AS QTDVEND

FROM
TGFCAB CAB

WHERE
CAB.CODTIPOPER IN ('3200','3205','3250','3262')) AAA

GROUP BY
AAA.PERIODO,
AAA.QTDVEND) BBB

GROUP BY
BBB.PERIODO
